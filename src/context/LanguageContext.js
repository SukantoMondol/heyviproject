// Language mapping for API
const languageMap = {
  'Dutch': 'nl',
  'English': 'en',
  'German': 'de',
  'French': 'fr',
  'Russian': 'ru',
  'Arabic': 'ar'
};

// Reverse mapping from API language IDs to language codes
const apiLanguageMap = {
  1: 'de', // German
  2: 'en', // English
  3: 'ru', // Russian
  4: 'ar', // Arabic
  5: 'fr'  // French
};

const LanguageContext = createContext();

export const useLanguage = () => {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
};

export const LanguageProvider = ({ children }) => {
  const [currentLanguage, setCurrentLanguage] = useState('en');
  const [languageName, setLanguageName] = useState('English');
  const [availableLanguages, setAvailableLanguages] = useState([]);
  const [isLoading, setIsLoading] = useState(false);

  // Initialize language from localStorage or default to English
  useEffect(() => {
    const savedLanguage = localStorage.getItem('hejvi_language');
    const savedLanguageName = localStorage.getItem('hejvi_language_name');
    
    if (savedLanguage && translations[savedLanguage]) {
      setCurrentLanguage(savedLanguage);
      setLanguageName(savedLanguageName || 'English');
    }
    
    // Check for available languages in localStorage (from login response)
    const savedAvailableLanguages = localStorage.getItem('hejvi_available_languages');
    if (savedAvailableLanguages) {
      try {
        const languages = JSON.parse(savedAvailableLanguages);
        setAvailableLanguages(languages);
      } catch (error) {
        console.error('Failed to parse saved available languages:', error);
      }
    }
  }, []);

  // Fetch available languages from API when user is authenticated
  const fetchAvailableLanguages = async () => {
    // This will be called by components that have access to auth context
    // For now, we'll rely on localStorage data from login

  };

  // Initialize languages when component mounts - only if no languages are loaded
  useEffect(() => {
    if (availableLanguages.length === 0) {
      fetchAvailableLanguages();
    }
  }, [availableLanguages.length]);

  const changeLanguage = async (languageName) => {
    try {
      setIsLoading(true);
      
      // Find the language object by name
      const languageObj = availableLanguages.find(lang => 
        lang.english_name === languageName || 
        lang.original_name === languageName ||
        lang.german_name === languageName
      );
      
      if (!languageObj) {
        console.error('Language not found:', languageName);
        return;
      }
      
      // Update language locally first for immediate UI response
      const languageCode = apiLanguageMap[languageObj.id];
      if (languageCode && translations[languageCode]) {
        setCurrentLanguage(languageCode);
        setLanguageName(languageObj.english_name || languageObj.original_name);
        
        // Save to localStorage
        localStorage.setItem('hejvi_language', languageCode);
        localStorage.setItem('hejvi_language_name', languageObj.english_name || languageObj.original_name);
        
        // Update document language
        document.documentElement.lang = languageCode;
        
        // Update document direction for RTL languages
        if (languageCode === 'ar') {
          document.documentElement.dir = 'rtl';
        } else {
          document.documentElement.dir = 'ltr';
        }
      }
      
      // Note: Server update will be handled by components that have auth context

      
    } catch (error) {
      console.error('Failed to change language:', error);
      // Revert to previous language if update failed
      const savedLanguage = localStorage.getItem('hejvi_language');
      const savedLanguageName = localStorage.getItem('hejvi_language_name');
      if (savedLanguage && translations[savedLanguage]) {
        setCurrentLanguage(savedLanguage);
        setLanguageName(savedLanguageName || 'English');
      }
    } finally {
      setIsLoading(false);
    }
  };

  const t = (key, params = {}) => {
    const translation = translations[currentLanguage]?.[key] || translations.en[key] || key;
    
    // Replace parameters in translation
    return translation.replace(/\{(\w+)\}/g, (match, param) => {
      return params[param] || match;
    });
  };

  const getAvailableLanguages = () => {
    // If we have API languages, use those, otherwise fall back to hardcoded ones
    if (availableLanguages.length > 0) {
      return availableLanguages.map(lang => ({
        id: lang.id,
        name: lang.english_name || lang.original_name,
        code: apiLanguageMap[lang.id] || 'en',
        flag: lang.flag_url || getLanguageFlag(apiLanguageMap[lang.id]),
        originalName: lang.original_name,
        germanName: lang.german_name,
        writingDirection: lang.writing_direction
      }));
    }
    
    // Fallback to hardcoded languages
    return Object.keys(languageMap).map(name => ({
      name,
      code: languageMap[name],
      flag: getLanguageFlag(languageMap[name])
    }));
  };

  const getAvailableLanguagesFromLocalStorage = () => {
    const savedAvailableLanguages = localStorage.getItem('hejvi_available_languages');
    if (savedAvailableLanguages) {
      try {
        const languages = JSON.parse(savedAvailableLanguages);
        return languages.map(lang => ({
          id: lang.id,
          name: lang.english_name || lang.original_name,
          code: apiLanguageMap[lang.id] || 'en',
          flag: lang.flag_url || getLanguageFlag(apiLanguageMap[lang.id]),
          originalName: lang.original_name,
          germanName: lang.german_name,
          writingDirection: lang.writing_direction
        }));
      } catch (error) {
        console.error('Failed to parse available languages from localStorage:', error);
        return [];
      }
    }
    return [];
  };

  const getLanguageFlag = (code) => {
    const flags = {
      'en': 'üá∫üá∏',
      'de': 'üá©üá™',
      'nl': 'üá≥üá±',
      'fr': 'üá´üá∑',
      'ru': 'üá∑üá∫',
      'ar': 'üá∏üá¶'
    };
    return flags[code] || 'üåê';
  };

  const value = {
    currentLanguage,
    languageName,
    changeLanguage,
    t,
    getAvailableLanguages,
    getAvailableLanguagesFromLocalStorage,
    getLanguageFlag,
    isLoading,
    availableLanguages
  };

  return (
    <LanguageContext.Provider value={value}>
      {children}
    </LanguageContext.Provider>
  );
};
